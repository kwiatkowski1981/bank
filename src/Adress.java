class Adress {
    String street;
    int homeNumber;
    int apartmentNumber;
    String city;
    int zipCode;

    Adress(String street, int homeNumber, int apartmentNumber, String city, int zipCode){
        this.street = street;
        this.homeNumber = homeNumber;
        this.apartmentNumber = apartmentNumber;
        this.city = city;
        this.zipCode = zipCode;
    }
}
